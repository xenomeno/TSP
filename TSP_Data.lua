local function TableToLuaCode(name, tbl, indent)
  indent = indent or ""
  
  local result = (name ~= "") and { indent .. name .. " =", indent .. "{" } or { indent .. "{" }
  local key_indent = indent .. "  "
  for key, value in pairs(tbl) do
    local key_name = tonumber(key) and string.format('%s[%d]', key_indent, key) or string.format('%s["%s"]', key_indent, key)
    if type(value) == "string" then
      table.insert(result, string.format('%s = "%s",', key_name, value))
    elseif type(value) == "number" then
      table.insert(result, string.format('%s = %s,', key_name, tostring(value)))
    elseif type(value) == "table" then
      local tbl_code = TableToLuaCode("", value, key_indent)
      table.insert(result, string.format('%s =\n%s,', key_name, tbl_code))
    end
  end
  table.insert(result, indent .. "}")
  
  return table.concat(result, "\n")
end

function ConvertTSPLib(name, src, opt, dest)
  local TSP = {}
  local header = true
  for line in io.lines(src) do
    if header then
      local name = string.match(line, "COMMENT : (.*)")
      if name then
        TSP.name = name
      end
      local coord_type = string.match(line, "EDGE_WEIGHT_TYPE : (.*)")
      if coord_type then
        if coord_type ~= "EUC_2D" then
          print("Only Euclidean 2D distance is supported")
          return
        end
        print("Reading Euclidean 2D distance format...")
      end
      if string.match(line, "NODE_COORD_SECTION") then
        header = false
      end
    else
      local city, x, y = string.match(line, "%s*(%d+)%s+([%d%.]+)%s+([%d%.]+)")
      if city and x and y then
        TSP[tonumber(city)] = { tonumber(x), tonumber(y) }
      end
    end
  end
  print(string.format("%d cities read", #TSP))
  
  TSP.OptimalTour, TSP.OptimalTourDistance = {}, 0
  local header = true
  local last_city
  for line in io.lines(opt) do
    if header then
      if string.match(line, "TOUR_SECTION") then
        header = false
      end
    else
      for city in string.gmatch(line, "%d+") do
        if city == "-1" then break end
        city = tonumber(city)
        table.insert(TSP.OptimalTour, city)
        if last_city then
          TSP.OptimalTourDistance = TSP.OptimalTourDistance + math.floor(Euc2D(TSP[last_city], TSP[city]) + 0.5)
        end
        last_city = city
      end
    end
  end
  TSP.OptimalTourDistance = TSP.OptimalTourDistance + math.floor(Euc2D(TSP[last_city], TSP[TSP.OptimalTour[1]]) + 0.5)
  print(string.format("Optimal Tour: %s", table.concat(TSP.OptimalTour, ",")))
  print(string.format("Optimal Tour Distance: %d", TSP.OptimalTourDistance))
  
  local f = io.open(dest, "w")
  f:write(TableToLuaCode(name, TSP))
  f:flush()
  f:close()
end

